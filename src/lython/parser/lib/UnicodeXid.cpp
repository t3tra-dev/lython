template <std::size_t N>
bool contains(const std::array<Range, N> &ranges, std::uint32_t codepoint) {
  const auto it = std::lower_bound(ranges.begin(), ranges.end(), codepoint,
                                   [](const Range &range, std::uint32_t value) {
                                     return range.last < value;
                                   });
  return it != ranges.end() && it->first <= codepoint;
}

