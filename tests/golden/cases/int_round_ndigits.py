"""round(int, ndigits) matches CPython at any width: identity for ndigits
>= 0, otherwise exact round-half-even against 10**-ndigits, including the
quotient*scale results that overflow a signed 64-bit lane."""

print(round(12345))
print(round(12345, 2))
print(round(12345, -2))
print(round(-12345, -2))
print(round(15, -1))
print(round(25, -1))
print(round(-15, -1))
print(round(-25, -1))
print(round(5, -1))
print(round(149, -2))
print(round(150, -2))
print(round(250, -2))
print(round(123, -100))
print(round(0, -5))
print(round(9223372036854775807, -1))
print(round(-9223372036854775808, -1))
print(round(9223372036854775807, -18))
print(round(9223372036854775807, -19))
print(round(123456789012345678901234567890123456789, -20))
print(round(123456789012345678901234567890123456789, -37))
print(round(123456789012345678901234567890123456789, -38))
print(round(123456789012345678901234567890123456789, -39))
print(round(-123456789012345678901234567890123456789, -20))
print(round(10**50 + 5 * 10**19, -20))
print(round(10**50 + 15 * 10**19, -20))
print(round(10**50, 5))
big = 123456789012345678901234567890
print(round(big, 0))
