# float の format spec mini-language を CPython 3.14 の実出力でピン留めする。
# e/E/f/F/g/G/n/%/型なし、precision、grouping、zero-pad regroup、inf/nan。
pi = 3.14159
print(f"{pi:f}")
print(f"{pi:.2f}")
print(f"{pi:.0f}")
print(f"{pi:e}")
print(f"{pi:.3e}")
print(f"{pi:E}")
print(f"{pi:g}")
print(f"{pi:.3g}")
print(f"{pi:G}")
print(f"{pi:%}")
print(f"{pi:.1%}")
print(f"{pi:10.2f}|")
print(f"{pi:<10.2f}|")
print(f"{pi:^10.2f}|")
print(f"{pi:=10.2f}")
print(f"{pi:+.2f}")
print(f"{pi: .2f}")
print(f"{pi:010.2f}")
print(f"{pi:#.0f}")
print(f"{pi:#g}")
print(f"{pi:.20f}")
print(f"{pi:.17e}")
neg = -3.14159
print(f"{neg:=10.2f}")
print(f"{neg:010.2f}")
print(f"{neg:*^12.3e}")
# 型なし (repr スタイル) と precision つき型なし ('g' + 最低1桁の点)
print(f"{pi:.3}")
print(f"{pi:.0}")
print(f"{0.5:.0}")
print(f"{1.0:.2}")
print(f"{12345.6789:.2}")
big = 1e100
print(f"{big:.3e}")
print(f"{big:#g}")
print(f"{big:,.2f}")
# grouping + zero-pad は桁詰めしなおす (CPython の regroup 規則)
v = 1234567.891
print(f"{v:,.2f}")
print(f"{v:020,.2f}")
print(f"{v:0=15,.1f}")
print(f"{v:*=15,.1f}")
print(f"{v:015,.2e}")
print(f"{v:0,.0f}")
# g/G の閾値と trailing zero strip
print(f"{0.0001:g}")
print(f"{0.00001:g}")
print(f"{123456:g}")
print(f"{1234567:g}")
print(f"{99.999:.3g}")
print(f"{99.999:.2g}")
print(f"{150.0:g}")
print(f"{0.0:g}")
print(f"{0.0:#g}")
# 丸めは half-even
print(f"{0.5:.0f}")
print(f"{1.5:.0f}")
print(f"{2.5:.0f}")
print(f"{2.675:.2f}")
print(f"{9.99:.1e}")
print(f"{2.5:.0e}")
print(f"{3.5:.0e}")
# 'z' は負ゼロ結果の符号を落とす
print(f"{-0.0001:z.2f}")
print(f"{-0.0:z.1f}")
print(f"{-0.0:.1f}")
# inf / nan
inf = 1e309
neg_inf = -1e309
nan = inf - inf
print(f"{inf:f}")
print(f"{inf:F}")
print(f"{inf:010.2f}")
print(f"{inf:+f}")
print(f"{neg_inf:010.2f}")
print(f"{nan:F}")
print(f"{nan:%}")
print(f"{nan:+f}")
# ゼロと % / n
print(f"{0.0:%}")
print(f"{0.5:%}")
print(f"{1234.5:n}")
